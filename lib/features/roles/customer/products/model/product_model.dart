class ProductModel {
  final String name;
  final String farmName;
  final String image;
  final double price;
  final String unit;
  final double rating;
  final String description;
  final double distance;

  ProductModel({
    required this.name,
    required this.farmName,
    required this.image,
    required this.price,
    required this.unit,
    required this.rating,
    required this.description,
    required this.distance,
  });
}
